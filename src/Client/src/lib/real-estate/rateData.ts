export function getRateTrend(): { direction: 'up' | 'down' | 'flat'; change: number; period: string } {
  const recent = rateHistory.slice(-4);
  const older = rateHistory.slice(-13, -4);
  const recentAvg = recent.reduce((s, r) => s + r.rate30yr, 0) / recent.length;
  const olderAvg = older.reduce((s, r) => s + r.rate30yr, 0) / older.length;
  const change = recentAvg - olderAvg;

  return {
    direction: change > 0.05 ? 'up' : change < -0.05 ? 'down' : 'flat',
    change: Math.abs(change),
    period: '3 months',
  };
}
